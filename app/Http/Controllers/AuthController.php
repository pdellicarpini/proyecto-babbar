<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use App\Models\User;
use Illuminate\Support\Facades\Hash;

class AuthController extends Controller
{
    public function login()
    {
        return view('auth.login');
    }

    public function process(Request $request)
    {
        $credentials = $request->only('email', 'password');

        if(Auth::attempt($credentials) === false) {
            return redirect()
                ->route('login')
                ->withInput()
                ->with('feedback.message', 'Credenciales incorrectas, por favor intenta de nuevo')
                ->with('feedback.type', 'danger');
        }

        return redirect()
            ->route('catalog')
            ->with('feedback.message', 'Has iniciado sesión correctamente');
    }

    public function register()
    {
        return view('auth.register');
    }

    public function store(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users',
            'password' => 'required|string|confirmed|min:8'
        ], [
            'name.required' => 'El nombre es obligatorio.',
            'email.required' => 'El correo electrónico es obligatorio.',
            'email.email' => 'El correo electrónico debe ser una dirección de correo válida.',
            'email.unique' => 'El correo electrónico ya está registrado.',
            'password.required' => 'La contraseña es obligatoria.',
            'password.confirmed' => 'Las contraseñas no coinciden.',
            'password.min' => 'La contraseña debe tener al menos 8 caracteres.'
        ]);

        User::create([
            'name' => $data['name'],
            'email' => $data['email'],
            'password' => Hash::make($data['password']),
        ]);

        return redirect()
            ->route('login')
            ->with('feedback.message', 'Cuenta creada correctamente')
            ->with('feedback.type', 'success');
    }

    public function logout(Request $request)
    {
        Auth::logout();

        $request->session()->invalidate();
        $request->session()->regenerateToken();

        return redirect()
            ->route('home')
            ->with('feedback.message', 'Has cerrado sesión correctamente');
    }
}
